    <h1 style="text-align:center;">D-III MANAJEMEN INFORMATIKA</h1>
    <hr>
    <p><b>Program Studi Manajemen Informatika, Konsentrasi Sistem Informasi</b> dirancang secara khusus guna 
    menghasilkan tenaga ahli madya bidang sistem informasi, yang memiliki kompetensi bidang manajerial, 
    desain, dan pembuatan basis data, proses bisnis, dan prosedur bisnis dalam bentuk perangkat lunak, yang 
    berkarakter technopreneur, sadar mutu dengan keunggulan kompetitif yang beretika profesi. </p>

    <p><b>Program Studi Manajemen Informatika, Konsentrasi Teknik Komputer dan Jaringan</b> dirancang secara 
    khusus guna menghasilkan tenaga ahli madya bidang teknik komputer dan jaringan, yang memiliki 
    kompetensi bidang manajerial, desain, dan pembuatan sistem jaringan dan perangkat pendukungnya, yang 
    berkarakter technopreneur, sadar mutu dengan keunggulan kompetitif yang beretika profesi.</p>

    <p><b>Program Studi Manajemen Informatika, Konsentrasi Multimedia</b> dirancang secara khusus guna 
    menghasilkan tenaga ahli madya bidang multimedia, yang memiliki kompetensi bidang manajerial, desain,
     dan pembuatan perangkat lunak berupa pengolahan teks, audio, video, image, animasi perangkat mobile, 
     desktop, dan jaringan, yang berkarakter technopreneur, sadar mutu dengan keunggulan kompetitif yang 
     beretika profesi.</p>

